weekDay(index) {
  if (index == 7) {
    return 'Sunday';
  } else if (index == 1) {
    return 'Monday';
  } else if (index == 2) {
    return 'Tuesday';
  } else if (index == 3) {
    return 'Wednesday';
  } else if (index == 4) {
    return 'Thursday';
  } else if (index == 5) {
    return 'Friday';
  } else if (index == 6) {
    return 'Saturday';
  }
}
